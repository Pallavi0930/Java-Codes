 class DemoWhileLoop{
	public static void main(String[] args){
		int num = 1;
		System.out.println("Number from 1 to 50");
		while(num<=50){
			System.out.print(num+",  ");
			num=num+1;
		}
		int num2 = 50;
		System.out.println();
		System.out.println("Number from 1 to 50");
		while(num2>0){
			System.out.print(num2+",  ");
			num2 = num2-1;
		}
		
		
	}
}